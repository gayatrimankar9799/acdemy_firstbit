//Sum of numbers in given range.
//Find sum of numbers from start to end.
//Input: start = 1, end = 5
//Output: 15

#include<stdio.h>
void main()
{
	int start=1;
	int end=5;
	int sum=0;
	for(start=1;start<=end;start++)
	{
		sum=sum+start;
	
	}

		printf("Sum of start and end is :%d",sum);
	
	
}